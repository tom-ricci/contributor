# This file contains commands to set your Git email and name. Contributor will use these credentials to commit your commits from your sources to your copy of the Contributor repository. This script will be executed right before Contributor commits. In theory you could write other code here to run inside the Contributor workflow, but I'd recommend editing the workflow file directly.

git config --global user.email "yourname@example.com"
git config --global user.name "yourname"
